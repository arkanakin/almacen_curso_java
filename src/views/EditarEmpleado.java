        txtNombre.setText(tblBusquedaEmpleado.getModel().getValueAt(tblBusquedaEmpleado.getSelectedRow(), 1).toString());
        txtApellidos.setText(tblBusquedaEmpleado.getModel().getValueAt(tblBusquedaEmpleado.getSelectedRow(), 2).toString());
        txtDireccion.setText(tblBusquedaEmpleado.getModel().getValueAt(tblBusquedaEmpleado.getSelectedRow(), 3).toString());
        txtTelefono.setText(tblBusquedaEmpleado.getModel().getValueAt(tblBusquedaEmpleado.getSelectedRow(), 4).toString());
