    public void consultaEmpleado(String nombre){
        String encabezados[] = {"Id", "Nombre","Apellidos", "Direccion", "Telefono", "Puesto", "Correo"};
        String fila[] = new String[7];
        consulta = new DefaultTableModel (null, encabezados);
        
        ConexionMySQL mysql = new ConexionMySQL();
        Connection conexion = mysql.conectar();
        
        String query = "SELECT * FROM empleado WHERE nombre LIKE '%"+nombre+"%'";
        
        Statement st;
        try {
            st = conexion.createStatement();
            ResultSet rs = st.executeQuery(query);
            while(rs.next()){
                fila[0]=rs.getString("id_empleado");
                fila[1]=rs.getString("nombre");
                fila[2]=rs.getString("apellidos");
                fila[3]=rs.getString("direccion");
                fila[4]=rs.getString("telefono");
                fila[5]=rs.getString("puesto");
                fila[6]=rs.getString("correo");
                consulta.addRow(fila);
            }
            tblBusquedaEmpleado.setModel(consulta);
        } catch (SQLException ex) {
            Logger.getLogger(Empleados.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }
    
